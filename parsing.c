#include "minishell.h"

int get_end(char *line, int i)
{
	if ("\"")
	{
      while (line[i])
      {
	      if (line[i] == '\"')
	      	return (i);
	      i++;
      }
	}
	else
	{
	  while (line[i])
      {
	      if (line[i] == '\'')
	      	return (i);
	      (i)++;
      }
	}
	return 0;
}
